    private void btn_student_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_student_loginActionPerformed

        Student_Login ulog = new Student_Login();
        Student_Interface mForm = new Student_Interface();

        try {
            if (txt_student_uname.getText().isEmpty()) {
                JOptionPane.showMessageDialog(ulog, "User Name Can not be null", "Warning", JOptionPane.WARNING_MESSAGE);
                txt_student_uname.grabFocus();

            } else if (pwd_student.getPassword().length == 0) {
                JOptionPane.showMessageDialog(ulog, "Password Can not be null", "Warning", JOptionPane.WARNING_MESSAGE);
                pwd_student.grabFocus();

            } else {
                
                if(txt_student_uname.getText().equals("student") && String.valueOf(pwd_student.getPassword()).equals("12345678"))
                {
                    this.dispose();
                    mForm.setVisible(true);
                }
                else{
                    JOptionPane.showMessageDialog(this, "Please Check Your Username & Password Again");
                    System.out.println(txt_student_uname.getText());
                    System.out.println(pwd_student.getPassword());
                }
                
            }
        } catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Warning", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btn_student_loginActionPerformed

